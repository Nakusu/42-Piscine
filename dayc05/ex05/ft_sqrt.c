int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb < 0)
		return (0);
	else
	{
		while (i < (nb / 2 - 1))
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
