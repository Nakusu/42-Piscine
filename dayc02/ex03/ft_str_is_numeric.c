#include <unistd.h>

int		ft_str_is_numeric(char *str)
{
	if (*str == 0)
		return (1);
	while (*str > 0)
	{
		if (*str >= 49 && *str <= 57)
		{
			str++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
