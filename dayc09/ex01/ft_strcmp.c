int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	while (s2[i])
	{
		if (s1[i] == 0)
			return (-s2[i]);
		i++;
	}
	return (0);
}
