#include <unistd.h>

void	writenb(char c1, char c2, char c3)
{
	write(1, &c1, 1);
	write(1, &c2, 1);
	write(1, &c3, 1);
	if (c1 != '7' || c2 != '8' || c3 != '9')
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb(void)
{
	char c1;
	char c2;
	char c3;

	c1 = '0';
	c2 = '0';
	c3 = '0';
	while (c1 <= '7')
	{
		c2 = c1 + 1;
		while (c2 <= '8')
		{
			c3 = c2 + 1;
			while (c3 <= '9')
			{
				writenb(c1, c2, c3);
				c3++;
			}
			c2++;
		}
		c1++;
	}
}
